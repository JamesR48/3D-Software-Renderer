#include "Swap.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Value swapping helper functions
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Integer_Swap(int* A, int* B)
{
	int Temp = *A;
	*A = *B;
	*B = Temp;
}

void Float_Swap(float * A, float * B)
{
	float Temp = *A;
	*A = *B;
	*B = Temp;
}